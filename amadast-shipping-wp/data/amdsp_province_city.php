function amdsp_get_province_name($province_id): string {
    $original_province_id = $province_id;
    $province_id = array_search($province_id, amdsp_get_wp_amdsp_province_map());

    if (empty($province_id))
        $province_id = AMDSP_Province_City::normalize_id($original_province_id);

    $provinces = amdsp_get_provinces();

    if (!key_exists($province_id, $provinces)) return $original_province_id;

    return $provinces[$province_id];
}

