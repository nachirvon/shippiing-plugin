function amdsp_get_province_cities($province_id): array {
    $province_id = AMDSP_Province_City::normalize_id($province_id);

    $provinces_cities = amdsp_get_provinces_cities();

    if (!key_exists($province_id, $provinces_cities)) return [];

    return array_key_exists($province_id, $provinces_cities) ? $provinces_cities[$province_id] : [];
}

function amdsp_get_all_cities() {
    return amdsp_array_flatten(amdsp_get_provinces_cities());
}

function amdsp_get_city_name($city_id): string {
    $city_id = AMDSP_Province_City::normalize_id($city_id);

    $cities = amdsp_get_all_cities();

    if (!key_exists($city_id, $cities)) return $city_id;

    return $cities[$city_id];
}

function amdsp_get_provinces_cities_with_full_name() {
    $provinces_cities = amdsp_get_provinces_cities();

    $cities = [];
    foreach ($provinces_cities as $province_id => $province_cities) {
        $province_name = amdsp_get_province_name($province_id);

        foreach ($province_cities as $city_id => $city_name) {
            $cities[$city_id] = "$province_name - $city_name";
        }
    }

    return $cities;
}
